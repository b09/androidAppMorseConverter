package com.example.macbook.mymenuproject;

public enum MorseAlphabet {

    A(".-", "a", "01100001", "א"),
    B("-...", "b", "01100010", "ב"),
    C("-.-.", "c", "01100011", "ש"),
    D("-..", "d", "01100100", "ד"),
    E(".", "e", "01100101", "י"),
    F("..-.", "f", "01100110", "פ"),
    G("--.", "g", "01100111", "ג"),
    H("....", "h", "01101000", "ה"),
    I("..", "i", "01101001", "י"),
    J(".---", "j", "01101010", "ע"),
    K("-.-", "k", "01101011", "כ"),
    L(".-..", "l", "01101100", "ל"),
    M("--", "m", "01101101", "מ"),
    N("-.", "n", "01101110", "נ"),
    O("---", "o", "01101111", "ו"),
    P(".--.", "p", "01110000", "פּ"),
    Q("--.-", "q", "01110001", "ק"),
    R(".-.", "r", "01110010", "ר"),
    S("...", "s", "01110011", "ס"),
    T("-", "t", "01110100", "ת"),
    U("..-", "u", "01110101", "ו"),
    V("...-", "v", "01110110", "ו"),
    W(".--", "w", "01110111", ""),
    X("-..-", "x", "01111000", ""),
    Y("-.--", "y", "01111001", "י"),
    Z("--..", "z", "01111010", "ז"),
    ONE(".----", "1", "00110001", "1"),
    TWO("..---", "2", "00110010", "2"),
    THREE("...--", "3", "00110011", "3"),
    FOUR("....-", "4", "00110100", "4"),
    FIVE(".....", "5", "00110101", "5"),
    SIX("-....", "6", "00110110", "6"),
    SEVEN("--...", "7", "00110111", "7"),
    EIGHT("---..", "8", "00111000", "8"),
    NINE("----.", "9", "00111001", "9"),
    ZERO("-----", "0", "00110000", "0"),
    QUOTE(".-..-.", "\"", "01011100", "\""),
    DOLLAR("...-..-", "$", "00100100", "$"),
    APOSTROPHE(".----.", "'", "00100111", "'"),
    OPENPAREN("-.--.", "(", "00101000", "("),
    CLOSEPAREN("-.--.-", ")", "00101001", ")"),
    OPENBRACKET("-.--.", "[", "01011011", "["),
    CLOSEBRACKET("-.--.-", "]", "01011101", "]"),
    PLUS(".-.-.", "+", "00101011", "+"),
    COMMA("--..--", ",", "00101100", ","),
    HYPHEN("-....-", "-", "00101101", "-"),
    PERIOD(".-.-.-", ".", "00101110", "."),
    SLANT("-..-.", "/", "00101111", "/"),
    COLON("---...", ":", "00111010", ":"),
    SEMICOLON("-.-.-.", ";", "00111011", ";"),
    EQUALS("-...-", "=", "00111101", "="),
    QUESTION("..--..", "?", "00111111", "?"),
    ATSIGN(".--.-.", "@", "01000000", "@"),
    EXCLAMATION("---.", "!", "00100001", "!"),
    SPACE(" ", " ", "00100000", " ");

    private String morse;
    private String alphabet;
    private String binary;
    private String hebrew;

    MorseAlphabet(String morse, String alphabet, String binary, String hebrew) {
        this.morse = morse;
        this.alphabet = alphabet;
        this.binary = binary;
        this.hebrew = hebrew;
    }

    public String getMorse() {
        return morse;
    }

    public String getAlphabet() {
        return alphabet;
    }

    public String getBinary() {
        return binary;
    }

    public String getHebrew() {
        return hebrew;
    }
}
